class Solution:
    def search(self, nums: list[int], target: int) -> int:
        # Указываем границы поиска: лево и право
        levo = 0
        pravo = len(nums) - 1
        
        while levo <= pravo:
            # Находим середину массива
            seredina = (levo + pravo) // 2
            
            # Если число в центре — это наша цель, возвращаем индекс
            if nums[seredina] == target:
                return seredina
            
            # Проверяем, какая часть массива (левая или правая) отсортирована правильно
            # Если число слева меньше или равно числу в центре — левая часть в порядке
            if nums[levo] <= nums[seredina]:
                # Проверяем, находится ли наша цель внутри этой «правильной» левой части
                if nums[levo] <= target < nums[seredina]:
                    # Если да, сужаем поиск до левой половины
                    pravo = seredina - 1
                else:
                    # Если нет, значит цель где-то в правой половине
                    levo = seredina + 1
            # Иначе, если левая часть «сломана», значит правая точно правильная
            else:
                # Проверяем, находится ли наша цель внутри «правильной» правой части
                if nums[seredina] < target <= nums[pravo]:
                    # Если да, ищем в правой половине
                    levo = seredina + 1
                else:
                    # Если нет, переходим в левую половину
                    pravo = seredina - 1
                    
        # Если весь массив проверили и ничего не нашли
        return -1
