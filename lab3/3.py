class Solution:
    def search(self, nums: list[int], target: int) -> int:
        # Устанавливаем границы поиска: лево и право
        levo = 0
        pravo = len(nums) - 1
        
        # Пока левая граница не встретилась с правой
        while levo <= pravo:
            # Находим индекс числа ровно посередине
            # Используем целочисленное деление //
            seredina = (levo + pravo) // 2
            
            # Если число в центре — это то, что мы ищем
            if nums[seredina] == target:
                # Возвращаем его индекс
                return seredina
            
            # Если число в центре меньше цели
            if nums[seredina] < target:
                # Значит все числа слева нам не подходят, ищем в правой части
                levo = seredina + 1
            # Если число в центре больше цели
            else:
                # Значит все числа справа слишком большие, ищем в левой части
                pravo = seredina - 1
                
        # Если цикл закончился, а число не нашли, возвращаем -1
        return -1
